    /**
     * Show the form for creating a new resource.
     *
     * @return \Illuminate\Http\Response
     */
    public function create()
    {   
        $routes = Route::orderBy('created_at', 'desc')->get();
        return view('dashboard.view-routes',compact('routes'));
    }

    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        $route=new Route();
        $route->route_from=$request->route_from;
        $route->route_to=$request->route_to;
        $route->amount=$request->amount;
        
        if ($route->save()) {
            return redirect()->back()->with('success', 'Route has been added successfully!'); 
        }else{
            return redirect()->back()->with('error', 'Route addition Failed!');
        }
        

    }

